void main(){
  /*
  ● Kadang ada kebutuhan kita ingin membuat variable yang bisa menampung semua jenis tipe data
  ● Pada kasus ini, kita bisa menggunakan tipe data dynamic
   */

  dynamic variable = 100;
  print(variable);

  variable = true;
  print(variable);

  variable = 'Fajar';
  print(variable);
}